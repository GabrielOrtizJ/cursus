#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*temporal;

	temporal = (*alst);
	if ((*alst))
	{
		while (temporal->next != NULL)
			temporal = temporal->next;
		temporal->next = new;
	}
	if (!(*alst))
		((*alst) = new);
}
